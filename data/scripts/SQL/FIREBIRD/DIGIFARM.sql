SELECT PRODUTO_ID referencia, t.TRIBUTACAO, PRODUTO nome, COD_BARRAS gtin, PROD_PRCOMPRA valorCompra, PROD_PRVENDA valorVenda,
CASE PROD_ATIVO
WHEN 'S' THEN 'VERDADEIRO'
WHEN 'N' THEN 'FALSO'
END AS produtoInativo,
CASE PROD_UNIDADE
WHEN 'UN' THEN 1
WHEN 'UND' THEN 1
WHEN 'KG' THEN 2
WHEN 'LT' THEN 3
WHEN 'CX' THEN 4
WHEN 'DZ' THEN 5
WHEN 'FD' THEN 6
WHEN 'GL' THEN 7
WHEN 'GR' THEN 8
WHEN 'BD' THEN 9
WHEN 'MT' THEN 10
WHEN 'M2' THEN 11
WHEN 'M3' THEN 12
WHEN 'MG' THEN 13
WHEN 'ML' THEN 14
WHEN 'MM' THEN 15
WHEN 'PC' THEN 16
END
AS idUnidade, CEST codigoCEST, NCM codigoNCM, COD_BARRAS_HOSPITALAR gtinTributavel, c.CATEGORIA observacao, sc.SUB_CATEGORIA descricaopdv, gc.GRUPO descricaoetiquetas, REGISTRO_MS localizacao
FROM PRODUTOS p
LEFT JOIN CATEGORIA c
ON p.CATEGORIA_ID = c.CATEGORIA_ID
LEFT JOIN SUB_CATEGORIAS sc 
ON p.SUB_CATEGORIA_ID = sc.SUB_CATEGORIA_ID
LEFT JOIN GRUPO_COMPRAS gc 
ON p.GRUPO_ID = gc.GRUPO_ID 
LEFT JOIN BENEFICIOS b 
ON b.BENEFICIO_ID = p.BENEFICIO_ID
LEFT JOIN TRIBUTACOES t 
ON t.TRIBUTACAO_ID = p.TRIBUTACAO_ID;