
PRODUTO

SELECT p.COD referencia, p.DSC nome, 
CASE p.PD_UNIDADE
WHEN 'UN' THEN 1
WHEN 'KG' THEN 2
WHEN 'LT' THEN 3
WHEN 'CX' THEN 4
WHEN 'DZ' THEN 5
WHEN 'FD' THEN 6
WHEN 'GL' THEN 7
WHEN 'GR' THEN 8
WHEN 'BD' THEN 9
WHEN 'MT' THEN 10
WHEN 'M2' THEN 11
WHEN 'M3' THEN 12
WHEN 'MG' THEN 13
WHEN 'ML' THEN 14
WHEN 'MM' THEN 15
WHEN 'PC' THEN 16
ELSE 1
END
AS idUnidade, p.PD_CEST codigoCEST, p.PD_NCM codigoNCM, pc.BARRAS, v.PRECO valorVenda, v.CUSTO valorCusto, v.ESTOQUE estoqueAtual,
CASE f.SIMPLES_TRIBUTACAO
WHEN '102' THEN 1
WHEN '500' THEN 2
WHEN '300' THEN 3
ELSE 1
END AS idFiscal
FROM SM_CD_ES_PRODUTO p
LEFT JOIN SM_CD_ES_PRODUTO_BAR pc
ON p.COD = pc.COD
LEFT JOIN SM_CD_ES_PRODUTO_DNM v
ON p.COD = v.COD 
LEFT JOIN SM_CD_ES_PRODUTO_EF_F f
ON p.COD = f.COD;


