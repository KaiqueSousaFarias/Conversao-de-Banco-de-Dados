PRODUTO

SELECT ID_PRODUTO referencia, NOME_PRODUTO nome, PRECO_PRODUTO valorVenda, CUSTO_PRODUTO valorCusto, QUANTIDADE estoqueAtual,
CASE UNIDADE
WHEN 'UN' THEN 1
WHEN 'UND' THEN 1
WHEN 'KG' THEN 2
WHEN 'LT' THEN 3
WHEN 'CX' THEN 4
WHEN 'DZ' THEN 5
WHEN 'FD' THEN 6
WHEN 'GL' THEN 7
WHEN 'GR' THEN 8
WHEN 'BD' THEN 9
WHEN 'MT' THEN 10
WHEN 'M2' THEN 11
WHEN 'M3' THEN 12
WHEN 'MG' THEN 13
WHEN 'ML' THEN 14
WHEN 'MM' THEN 15
WHEN 'PC' THEN 16
WHEN 'PCT' THEN 16
ELSE 1
END
AS idUnidade, NCM codigoNCM, CEST codigoCEST, SECAO observacao,
CASE CST
WHEN '102' THEN 1
WHEN '500' THEN 2
WHEN '300' THEN 3
ELSE 1
END
AS idFiscal, CODIGO_PRODUTO gtin
FROM PRODUTO;